var nome = prompt("Digite o seu nome:"); 
var n = parseInt(prompt("Olá"+" "+ nome + ". Digite um número:"));

document.write("Seja bem vindo..: "+ nome + "<br/>");

document.write("Você digitou o número..: ("+ n + ")<br/>");

document.write("O retorno da comparação booleana é..:"+ (n==n) + "<br/>");

document.write("A soma dos valores é..:"+ (n+n) +"<br/>");

document.write("A subtração dos valores..:"+ (n-n) +"<br/>");

document.write("O resto da divisão é..:"+ (n%n) +"<br/>");

document.write("O quadrado do número é..:"+ n*n); 